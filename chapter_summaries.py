def get_chapter_summary(chapter_number: int) -> dict:
    """
    Get the summary for a specific chapter.
    
    Args:
        chapter_number: Chapter number (1-18)
    
    Returns:
        Dictionary with title and summary, or None if invalid chapter
    """
    return CHAPTER_SUMMARIES.get(chapter_number)


def get_all_summaries() -> dict:
    """
    Get all chapter summaries.
    
    Returns:
        Dictionary of all chapter summaries
    """
    return CHAPTER_SUMMARIES


def get_summary_text(chapter_number: int) -> str:
    """
    Get formatted summary text for a chapter.
    
    Args:
        chapter_number: Chapter number (1-18)
    
    Returns:
        Formatted string with chapter title and summary
    """
    chapter = CHAPTER_SUMMARIES.get(chapter_number)
    if not chapter:
        return f"Chapter {chapter_number} not found. Please provide a chapter number between 1 and 18."
    
    return f"Chapter {chapter_number}: {chapter['title']}\n\n{chapter['summary']}"


